<p>DUFFA's aim is to ensure that new members are always made to feel welcome and level ensures that just about anyone can just turn up and join in. We welcome all abilities, especially those who have never played before. We're happy to take people new to Ultimate Frisbee through the basics - from how to throw and catch and go over the rules in a relaxed atmosphere.  We run regular "Beginners Sessions" but if you're interested in trying Ultimate feel free to turn up to any session in old trainers (or ideally football boots, especially if it's muddy) and bring a bottle of water. 
