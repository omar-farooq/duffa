<h2>Who we are</h2>
<p>We're Manchester's open Ultimate Frisbee club, one of the largest in the North West and organisers of DUFFA Hat. We meet and play twice a week - outdoors at Fog Lane Park, Didsbury, Manchester and indoors (winter only) at Parrs Wood High School Sports Hall.</p>

<p>Over the summer (April - August), we play every Thursday and Sunday and finish with a pint or three in the Fletcher Moss, Didsbury. Over the winter (September - April), we play every Sunday with a midweek session indoors.</p>

<p>Our players are of varying levels, from total beginners to uni-level. We also frequently meet up for socials in and around the Didsbury area.</p>
